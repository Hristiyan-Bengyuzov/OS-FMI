# Намерете имената на 10-те файла във вашата home директория, чието съдържание е
# редактирано най-скоро. На първо място трябва да бъде най-скоро редактираният файл.

find ~ -printf '%T@ %p\n' | sort -t ' ' -k 1 -nr | cut -d ' ' -f 2 | head

# Намерете 10-те най-скоро достъпени файлове. (hint: Unix time)

find ~ -printf '%A@ %p\n' | sort -t ' ' -k 1 -nr | cut -d ' ' -f 2 | head
